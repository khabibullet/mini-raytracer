#include "../../headers/vector.h"

void	get_rev_matrix(float rev[3][3], float rot[3][3])
{
	rev[0][0] = rot[0][0];
	rev[1][1] = rot[1][1];
	rev[2][2] = rot[2][2];
	rev[0][1] = rot[1][0];
	rev[1][0] = rot[0][1];
	rev[1][2] = rot[2][1];
	rev[2][1] = rot[1][2];
	rev[0][2] = rot[2][0];
	rev[2][0] = rot[0][2];
}
